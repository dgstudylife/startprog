#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	div;
	int	rest;
	char	c;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
		ft_putnbr(nb);
	}
	else
	{
		div = nb / 10;
		rest = nb % 10;
		if (div > 0)
			ft_putnbr(div);
		c = '0' + rest;
		write(1, &c, 1);
	}
}
